        ("acute", "acute", 1.0),
        ("arrowdown", "arrowdown", 1.5),
        ("arrowleft", "arrowleft", 1.5),
        ("arrowright", "arrowright", 1.5),
        ("arrowup", "arrowup", 1.5),
        ("B", "H", 1.25),
        ("C", "H", 1.25),
        ("D", "H", 1.25),
        ("dieresis", "dieresis", 1.0),
        ("dot", "dot", 1.5),
        ("E", "H", 1.25),
        ("F", "H", 1.25),
