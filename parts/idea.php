		<div class="medium-8 medium-push-3 columns">
			<h4>HT LetterSpacer is a tool for spacing fonts, that works on finished fonts as well as during development. The first public version works as a macro for <a href="https://glyphsapp.com">Glyphs</a> and uses that application's glyph categories and subcategories feature, but the method is adaptable to any editor or programming language.</h4>
		</div>
		<div class="medium-3 medium-pull-9 columns smaller">
			<p>With HT LetterSpacer making a consistent spacing is easy, because you can define fixed parameters of spacing during your type design process and focus on the letterform drawing. </p>
			<p>By making the spacing of a font simpler you can try different spacing options quickly without going mad. </p>
			<p>Today HT LetterSpacer doesn't kern, it is only for setting glyph widths and sidebearings.</p>
