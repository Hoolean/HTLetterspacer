					<div class="accordion-content" data-tab-content>
						<p>Andrés Torresi is the initial and primary author of this tool. Soon after joining the postgraduate course in typeface design in Buenos Aires in 2009 he began research to find a simple and semi-automated method to set glyph sidebearings, that could work at any moment during a project, and that the type designer can control. Today HT LetterSpacer is the result of this research, and has been thoroughly tested by Andrés and Juan Pablo del Peral at the Huerta Tipográfica foundry and other designers on Latin, Greek, Cyrillic, and Devanagari designs. Experiments with Latin fonts by several other type designers around the world were conducted during development, and each said that the HT LetterSpacer program produces great results.</p>
						<p>At the ATypI 2015 in São Paulo, Andrés approached Dave Crossland to discuss ways of funding the project to make it available under the GPLv3 license. Andrés wanted to invite other type designers and software developers to use, study, share, and improve the tool, because with peer review it can grow beyond what he can do by himself and become a basis for better and new approaches to font spacing. The tool was used and tested privately by the Google Fonts team and finally at ATypI 2016 in Warsaw Andrés and Georg Seifert announced that HT LetterSpacer is finally available for all Glyphs users from the Glyphs Plugin System with sources at <a href="https://github.com/huertatipografica/HTLetterspacer">https://github.com/huertatipografica/HTLetterspacer</a></p>
